export const listItemIds = query(
  async (): Promise<Record<string, { jellyfin: string }>> => {
    "use server";

    const { data, error } = await getClient().GET("/Items", {
      params: {
        query: {
          hasImdbId: true,
          recursive: true,
          includeItemTypes: ["Movie", "Series"],
          fields: [
            "ProviderIds",
            "Genres",
            "DateLastMediaAdded",
            "DateCreated",
            "MediaSources",
          ],
        },
      },
    });

    if (data === undefined) {
      return {};
    }

    return Object.fromEntries(
      data.Items?.map((item) => {
        const type = {
          Movie: 'movie',
          Series: 'tv',
        }[item.Type as string] ?? 'unknown';

        return [
          `${type}-${item.ProviderIds!["Tmdb"]!}`,
          { jellyfin: item.Id! },
        ];
      }) ?? []
    );
  },
  "jellyfin.listItemIds",
);

