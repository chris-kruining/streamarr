const getBaseUrl = () => {
  "use server";

  return process.env.JELLYFIN_BASE_URL;
};

const getClient = () => {
  "use server";

  return createClient<paths>({
    baseUrl: getBaseUrl(),
    headers: {
      Authorization: `MediaBrowser DeviceId="Streamarr", Token="${process.env.JELLYFIN_API_KEY}"`,
      "Content-Type": 'application/json; profile="CamelCase"',
    },
  });
};

